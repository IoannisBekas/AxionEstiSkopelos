function revealAll() {
  document.querySelectorAll(".reveal-item").forEach((element) => {
    element.classList.add("is-visible");
  });
}

function setupRevealObserver() {
  if (revealObserver) {
    revealObserver.disconnect();
  }

  const revealItems = document.querySelectorAll(".reveal-item");

  if (prefersReducedMotion.matches || !("IntersectionObserver" in window)) {
    revealAll();
    return;
  }

  revealObserver = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) {
          return;
        }

        entry.target.classList.add("is-visible");
        revealObserver.unobserve(entry.target);
      });
    },
    {
      threshold: 0.14,
      rootMargin: "0px 0px -10% 0px"
    }
  );

  revealItems.forEach((element) => {
    revealObserver.observe(element);
  });
}

function updateHeaderState() {
  const header = document.querySelector(".site-header");

  if (!header) {
    return;
  }

  header.classList.toggle("is-scrolled", window.scrollY > 18);
}

function buildParallaxItems() {
  parallaxItems = Array.from(document.querySelectorAll(".visual-card, .hero-note, .page-link, .route-step")).map((element, index) => {
    let strength = 0.022;

    if (element.classList.contains("visual-card")) {
      strength = 0.038;
    } else if (element.classList.contains("hero-note")) {
      strength = 0.028;
    } else {
      strength += (index % 3) * 0.003;
    }

    return { element, strength };
  });
}

function updateParallax() {
  const maxScroll = document.documentElement.scrollHeight - window.innerHeight;
  const progress = maxScroll > 0 ? window.scrollY / maxScroll : 0;
  const siteBackground = document.querySelector(".site-bg");

  document.body.style.setProperty("--scroll-progress", progress.toFixed(4));

  if (siteBackground) {
    const bgX = (progress - 0.5) * 18;
    const bgY = window.scrollY * -0.035;
    siteBackground.style.setProperty("--bg-shift-x", `${bgX.toFixed(2)}px`);
    siteBackground.style.setProperty("--bg-shift-y", `${bgY.toFixed(2)}px`);
  }

  if (prefersReducedMotion.matches) {
    return;
  }

  const viewportCenter = window.innerHeight / 2;

  parallaxItems.forEach(({ element, strength }) => {
    const rect = element.getBoundingClientRect();
    const elementCenter = rect.top + rect.height / 2;
    const offset = (viewportCenter - elementCenter) * strength;
    const limitedOffset = Math.max(-18, Math.min(18, offset));
    element.style.setProperty("--parallax-y", `${limitedOffset.toFixed(2)}px`);
  });
}

function scheduleParallaxUpdate() {
  if (parallaxTicking) {
    return;
  }

  parallaxTicking = true;

  window.requestAnimationFrame(() => {
    updateParallax();
    parallaxTicking = false;
  });
}

function handleMotionPreferenceChange() {
  if (prefersReducedMotion.matches) {
    revealAll();
    document.body.style.setProperty("--scroll-progress", "0");
    parallaxItems.forEach(({ element }) => {
      element.style.removeProperty("--parallax-y");
    });
    return;
  }

  setupRevealObserver();
  scheduleParallaxUpdate();
}

function setupMotion() {
  if (motionInitialized) {
    return;
  }

  motionInitialized = true;
  document.body.classList.add("js-enhanced");
  setupMediaCards();

  decorateMotionTargets();
  buildParallaxItems();
  setupRevealObserver();
  updateHeaderState();

  window.requestAnimationFrame(() => {
    document.body.classList.add("page-ready");
    scheduleParallaxUpdate();
  });

  window.addEventListener(
    "scroll",
    () => {
      updateHeaderState();
      scheduleParallaxUpdate();
    },
    { passive: true }
  );

  window.addEventListener("resize", scheduleParallaxUpdate);

  if (typeof prefersReducedMotion.addEventListener === "function") {
    prefersReducedMotion.addEventListener("change", handleMotionPreferenceChange);
  } else if (typeof prefersReducedMotion.addListener === "function") {
    prefersReducedMotion.addListener(handleMotionPreferenceChange);
  }
}

setupMotion();
