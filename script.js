function setupMediaCards() {
  document.querySelectorAll("[data-media]").forEach((mediaCard) => {
    const image = mediaCard.querySelector("img");

    if (!image) {
      return;
    }

    const markLoaded = () => {
      mediaCard.classList.add("has-media");
      mediaCard.classList.remove("media-missing");
    };

    const markMissing = () => {
      mediaCard.classList.add("media-missing");
      mediaCard.classList.remove("has-media");
    };

    if (image.complete) {
      if (image.naturalWidth > 0) {
        markLoaded();
      } else {
        markMissing();
      }
    }

    image.addEventListener("load", markLoaded);
    image.addEventListener("error", markMissing);
  });
}

function assignReveal(elements, options = {}) {
  const { start = 0, step = 0, variant = "" } = options;

  Array.from(elements).forEach((element, index) => {
    if (!element) {
      return;
    }

    element.classList.add("reveal-item");

    if (variant) {
      element.classList.add(variant);
    }

    element.style.setProperty("--reveal-delay", `${start + index * step}ms`);
  });
}

